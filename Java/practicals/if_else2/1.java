class demo1 {

	public static void main(String[] args){
	
		short num = 100;

		if(num>0 && num<=1000) {
	
			System.out.println(num+" is in the range 1 to 1000.");
		}
		else {
		
			System.out.println(num+" is not in the range 1 to 1000.");
		}
		
		num = 0;

		if(num>0 && num<=1000) {
	
			System.out.println(num+" is in the range 1 to 1000.");
		}
		else {
		
			System.out.println(num+" is not in the range 1 to 1000.");
		}
		
		num = -11;

		if(num>0 && num<=1000) {
	
			System.out.println(num+" is in the range 1 to 1000.");
		}
		else {
		
			System.out.println(num+" is not in the range 1 to 1000.");
		}
	}
}
